async function loadNotificationsOptimized(forceRefresh = false) {
  if (typeof API_URL === 'undefined') {
    return;
  }
  if (isLoading) return;
  isLoading = true;
  

  const container = document.getElementById("notifications");
  const hasCached = !forceRefresh && getCachedNotifications();
  if (!hasCached) showSkeletonNotifications();

  try {
    const token = sessionStorage.getItem("admin_token") || "";
    if (!token) throw new Error("No token");

    const params = new URLSearchParams({
      action: "notificationsBatch",
      page: "1",
      pageSize: "100",
      noCache: forceRefresh ? "true" : "false",
      token: token
    });
    const url = `${API_URL}?${params.toString()}`;
    
    const response = await fetch(url);
    const result = await response.json();

    if (!result || !result.ok) {
      throw new Error(result?.error || "Error");
    }

    setCachedNotifications(result);
    processAndRenderNotifications(result);

  } catch (err) {
    console.error(err);

    const fallback = getCachedNotifications();
    if (fallback && fallback.groups) {
      processAndRenderNotifications(fallback);
    } else if (container && !container.hasChildNodes()) {
      container.innerHTML = '<div class="empty">Sin solicitudes o error de conexión</div>';
    }
  } finally {
    if (typeof hideLoader === 'function') hideLoader();
    isLoading = false;
    hideSkeletonNotifications();
  }
}

